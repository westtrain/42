#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *ptr;
  	int i = 0;

	ptr = (char*)malloc(sizeof(*s1));
	while (s1[i])
  	{
    	ptr[i] = s1[i];
    	i++;    
  	}
  	ptr[i] = '\0';
	return (ptr);
}
