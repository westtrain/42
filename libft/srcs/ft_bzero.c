#incldue "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t i = 0;
	unsigned char *ptr;

	if (n != 0)
	{
		ptr = (unsigned char*)s;
		while (i < n)
		{
			ptr[i] = 0;
			i++;
		}
	}
}
